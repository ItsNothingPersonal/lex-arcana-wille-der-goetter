				<h3>Eine Aktion auflösen</h3>
				<p>
					Nicht jede Aktion muss durch eine Würfelprobe aufgelöst werden. Viele der möglichen
					Aufgaben während des Spiels stellen für tapfere Custodes keine besondere Herausforderung
					dar: ein Feuer zu entfachen, um etwas zu Essen zu kochen, eine Leiter hinaufzuklettern
					oder die örtlichen Stadtwachen um Hilfe zu bitten, sind zum Beispiel alles automatische
					Aktionen, die ohne Probe als erfolgreich gelten.
				</p>

				<p>
					Eine Würfelprobe wird nur dann benötigt, wenn ungünstige Umstände oder direkter Widerstand
					dafür sorgen, dass der Ausgang einer Aktion nicht sicher ist.
				</p>
				<p>
					Ist dies der Fall, wählt der Demiurgus das Attribut des handelnden Charakters aus, das am
					besten zur Situation passt. Dabei sollten die Beschreibungen sowohl von Virtutes als auch
					von Peritiae berücksichtigt werden. Meistens wird das passende Attribut eine Peritia sein,
					aber es kann vorkommen, dass für eine bestimmte Aufgabe eine Virtus benötigt wird. Wenn
					die Spielleitung sich nicht zwischen einer Virtus und einer Peritia entscheiden kann, wird
					auf das Attribut mit dem höheren Wert gewürfelt. Nachdem das Attribut bestimmt wurde, muss
					der Demiurgus noch den Schwierigkeitsgrad (SG) der Aktion festlegen.
				</p>
				<p>
					Dann nimmt die Person, die den Charakter führt, den Würfel oder die Würfelkombination, die
					zum gewählten Attribut gehört, und würfelt.
				</p>
				<ol class="text-justify">
					<li>
						<b>Das passende Attribut</b><br />
						Die überwiegende Mehrzahl der Aktionen in LEX ARCANA kann mit Hilfe von einer der sechs Peritiae
						aufgelöst werden, die die Fähigkeiten eurer Custodes beschreiben. Selten kann es aber auch
						vorkommen, dass eine Virtus sich besser eignet (siehe auch die Beschreibungen der besonderen
						Würfelproben unter jeder Virtus). Die Spielleitung sollte die Beschreibungen der verschiedenen
						Fertigkeiten gut durchlesen, damit sie genau weiß, welche Attribute in bestimmten Situationen
						infrage kommen.
					</li>
					<li>
						<b>Der Schwierigkeitsgrad (SG)</b><br />
						Wie schwierig eine Aktion ist, wird spielmechanisch mit einem Zahlenwert ausgedrückt, dem
						Schwierigkeitsgrad (SG). In diesen Schnellstarterregeln würfelt der Demiurgus jedes Mal,
						wenn der Erfolg einer Aktion wegen ungünstiger Umstände unsicher ist, 2W6. Die Summe ergibt
						den Schwierigkeitsgrad. Ist der Erfolg dagegen unsicher, weil es direkten Widerstand einer
						anderen Figur gibt, würfelt der Demiurgus auf das passende Attribut der gegnerischen Figur
						(das nennt man einen Widerstandswurf), um den Schwierigkeitsgrad festzulegen.
					</li>
					<li>
						<b>Würfeln</b><br />
						Sobald das Attribut und der Schwierigkeitsgrad der Aktion feststehen, würfelt der*die Spielende
						die Würfel, die dazu auf dem Charakterbogen aufgeführt sind. Ist das Ergebnis höher als der
						Schwierigkeitsgrad, ist die Aktion ein Erfolg, wenn nicht, ist sie schiefgegangen.
					</li>
				</ol>

