				</p>

				<h2>Würfel</h2>
				<p>
					Für LEX ARCANA braucht ihr ein Würfel-Set von je einem Würfel mit 4, 6, 8, 10, 12 und 20
					Seiten (abgekürzt als W4, W6, W8, W10, W12 und W20). In den Spielregeln kommen auch W3 und
					W5 vor, in diesen Fällen wird ein W6 bzw. ein W10 gewürfelt und das Ergebnis halbiert.
				</p>

				<h2>Würfelproben</h2>
				<p>
					Die Virtutes und Peritiae, die eine*n Custos definieren, ebenso wie seine*ihre Waffen und
					Rüstung, sind auf dem Charakterbogen als ein Zahlenwert ausgedrückt. Wenn im Verlauf des
					Spiels eine Würfelprobe verlangt wird, um zum Beispiel herauszufinden, ob eine Aktion
					erfolgreich war, wird dieser Zahlenwert in einen entsprechenden Würfel oder eine
					Kombination aus Würfeln übersetzt. Nutzt ihr eine Kombination aus Würfeln werden alle
					Ergebnisse zusammengezählt. Je höher das Ergebnis, desto besser.
				</p>

				<h3>Würfelpunkte</h3>
				<p>
					In Lex Arcana stellen die Werte für Attribute und Ausrüstung eine entsprechende Zahl
					Würfelpunkte dar. Ihr könnt diese Würfelpunkte in eine beliebige Kombination aus bis zu
					drei Würfeln übersetzen, deren höchst mögliche Ergebnisse in Summe dem Wert entsprechen.
					12 Würfelpunkte können also etwa in einen W12 oder 2W6 oder 3W4 oder auch in einen W8 und
					einen W4 übersetzt werden.
				</p>

				<h3>Schicksalswürfe</h3>
				<p>
					Wenn ihr mit einem oder mehreren Würfeln würfelt und dabei das höchstmögliche Ergebnis
					erzielt (zum Beispiel eine 8 auf einem W8 oder eine 15 auf einem W10 und einem W5),
					würfelt ihr den (oder die) gleichen Würfel erneut und zählt das zweite Ergebnis zum ersten
					hinzu. Fällt dabei noch einmal das höchstmögliche Ergebnis, dürft ihr danach noch einmal
					würfeln und das Ergebnis hinzuzählen, und so weiter. Diese Würfe heißen Schicksalswürfe.
					Alle Würfelwürfe, die Spielende für ihre Custodes ausführen, können Schicksalswürfe sein,
					auch Schadens- und Schutzwürfe (siehe Der Kampf).
				</p>

				<h3>Eine Schutzgottheit anrufen</h3>
				<p>
					Der göttliche Schutz, unter dem die Cohors Arcana steht, zeigt sich nicht nur in
					Schicksalswürfen, sondern auch darin, dass die Custodes die Schutzgottheit ihres Cursus,
					Numen genannt, um Hilfe bitten können. Spielmechanisch gehört zu jedem Numen eine Peritia,
					nämlich die Hauptperitia des entsprechenden Cursus:
