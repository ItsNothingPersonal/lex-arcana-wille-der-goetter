<section class="container mx-auto text-center py-6 mb-12">
	<h1>Custodes</h1>
	<div class="w-full mb-4">
		<div class="h-1 mx-auto bg-white w-1/6 opacity-25 my-0 py-0 rounded-t" />
	</div>
	<p>
		In LEX ARCANA schlüpft ihr in die Rolle von Custodes (deutsch: Wächter*innen, Singular:Custos)
		der Cohors Auxiliaria Arcana, einer Spezialeinheit der Prätorianergarde, die vom Kaiser
		persönlich beauftragt wurde, übernatürliche Ereignisse zu finden und zu erforschen. Eure Mission
		ist sogar noch klarer: Ihr sollt alle okkulten und magischen Aktivitäten aufspüren und, wenn
		möglich, unterbinden.
	</p>
	<p>
		Denn hinter der Fassade aus Luxus und militärischer Macht des Römischen Reichs nagen viele
		arkane Bedrohungen an seinen Wurzeln: Verbotene Kulte konvertieren die ärmsten und
		unzufriedensten Bewohner*innen der großen Städte, während feindliche Zauberkundige immer wieder
		furchteinflößende Monster auf die Legionen an den Grenzen des Reichs hetzen.
	</p>
	<p>
		Um ihre Mission zu erfüllen, müssen die Custodes der Cohors Arcana in alle vier Ecken der
		bekannten Welt reisen, ungewöhnliche Vorkommnisse untersuchen, sich der oft abweisenden
		Bevölkerung stellen oder die Geheimnisse hinter fremdartigen Zaubern enthüllen.
	</p>
	<p>
		Weit vom Zentrum der kaiserlichen Macht entfernt müssen sie nach eigenem Gutdünken handeln und
		riskieren dabei, Unruhen in Städten oder sogar ganzen Provinzen auszulösen, sollten sie
		scheitern. Oft müssen sie kämpfen, um ihre Missionen zu erfüllen, müssen gar Mordanschlägen
		entkommen oder sich Kreaturen aus der Unterwelt stellen.
	</p>
	<p>
		Die Abenteuer in LEX ARCANA sind so unterschiedlich wie das Römische Reich groß ist. Die
		Custodes können in die Katakomben von Rom geschickt werden, um ein verlorenes Relikt zu finden,
		sie können einen unheimlichen Kult untersuchen, der eine Garnison in Britannien unterwandert
		hat, sie können in Syria untote Monster bekämpfen oder zu den verschneiten Küsten von Hyperborea
		segeln, wo die Riesen leben und die Sonne niemals untergeht.
	</p>

	<h2>Die Cohors Auxiliaria Arcana</h2>
	<p>
		Die Cohors Auxiliaria Arcana (deutsch: arkane Unterstützungstruppen) besteht aus ein paar
		hundert hochspezialisierten und besonders begabten Personen, sowohl aus dem Militär als auch der
		Zivilbevölkerung stammend. Anwerber wählen sie aus den Bewohner*innen der vielen Provinzen des
		Reichs aus. Dann werden alle Rekrut*innen von einer Kommission bewertet und einem der fünf
		Ausbildungspfade zugeteilt.
	</p>

	<h3>Das Tirocinium</h3>
	<p>
		Nach der Aufnahme in die Cohors Arcana müssen alle zukünftigen Custodes an einem
		Ausbildungsprogramm, dem Tirocinium, teilnehmen, um ihre Fähigkeiten auszubauen und ihre Wissen
		zu erweitern. Am Ende der Ausbildung wird jede*r Custos in die Geheimnisse des Kultes einer
		spezifischen römischen Gottheit, Numen genannt, eingeweiht. Ab diesem Moment steht er*sie unter
		dem besonderen Schutz dieser Gottheit.
	</p>
	<h3>Das Contubernium</h3>
	<p>
		Custodes mit unterschiedlichen Ausbildungen werden zu einem Contubernium (deutsch:
		Zeltgemeinschaft) zusammengestellt, einer Gruppe, die für gewöhnlich 3 bis 6 Mitglieder umfasst.
		Sie trainieren gemeinsam und lernen, zu einer kleinen, besonders mobilen und autarken Einheit zu
		werden. Sie agieren meist ohne direkte Befehlshaber*innen und müssen immer darauf vorbereitet
		sein, sich unerwarteten Gefahren zu stellen. Wenn man ihren Ausbildern und Vorgesetzten Glauben
		schenken darf, sind Custodes gänzlich anders als normale Legionäre, die sich an die üblichen
		Befehlsketten halten müssen. Custodes sollen sich selbst als Offizier*innen betrachten, die die
		volle Autorität des Kaisers vertreten. Noch besser ist es, wenn sie sich selbst in eine Reihe
		mit den abenteuerlustigen Helden der alten Legenden und Gedichte stellen, mit Achilles, Odysseus
		und Aeneas, die vom Schicksal für eine höhere Bestimmung auserwählt wurden.
	</p>
</section>
