		Gelehrte*r, Krieger*in, Kundschafter*in oder Seher*in.
	</p>
	<h2>Diplomat*In</h2>
	<p>
		Diesen Titel erhalten Custodes, die den Cursus Legatorius, die Diplomat*innenLaufbahn,
		absolviert haben. Sie zeichnen sich durch besonders gut geschulte zwischenmenschliche
		Fertigkeiten, Redegewandtheit, Diplomatie und Rhetorik aus. Der Schutzgott aller Diplomat*innen
		ist Mercurius.
	</p>
	<h2>Gelehrte*r</h2>
	<p>
		Diesen Titel erhalten Custodes, die den Cursus Sapiens, die Gelehrten-Laufbahn, absolviert
		haben. Kern ihrer Ausbildung war, die vielen Bereiche des akademischen Wissens auf den Erfolg
		ihrer Missionen anzuwenden. Die Schutzgöttin aller Gelehrten ist Minerva.
	</p>
	<h2>Krieger*in</h2>
	<p>
		Diesen Titel erhalten Custodes, die den Cursus Bellicus, die Krieger*innen-Laufbahn, absolviert
		haben. Dort lernten sie alles über die Kriegskunst. Der Schutzgott aller Krieger*innen ist Mars.
	</p>
	<h2>Kundschafter*in</h2>
	<p>
		Diesen Titel erhalten Custodes, die den Cursus Exploratorius, die Kundschafter*innen-Laufbahn,
		absolviert haben. Sie haben gelernt, wie man in unwirtlichem Gelände überlebt, die Spuren einer
		Beute liest und einen Weg für andere bahnt. Die Schutzgöttin aller Kundschafter*innen ist Diana.
	</p>
	<h2>Seher*in</h2>
	<p>
		Diesen Titel erhalten Custodes, die den Diesen Titel erhalten Custodes, die den Cursus
		Auguralis, die Seher*innen-Laufbahn, absolviert haben. Dort haben sie die Kunst der Weissagung
		in Theorie und Praxis erlernt. Der Schutzgott aller Seher*innen ist Apollo.
	</p>

