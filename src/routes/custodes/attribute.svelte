<section class="container mx-auto text-center py-6 mb-12">
	<h1>Attribute</h1>
	<div class="w-full mb-4">
		<div class="h-1 mx-auto bg-white w-1/6 opacity-25 my-0 py-0 rounded-t" />
	</div>
	<p>
		Alle körperlichen, geistigen und spirituellen Attribute eines*einer Custos lassen sich in eine
		von zwei Kategorien einordnen. Zu jedem Attribut gehört ein Zahlenwert, durch den die
		Fähigkeiten des Charakters im Spiel beschrieben werden.
	</p>
	<h2>Virtutes</h2>
	<p>
		Die Virtutes (deutsch: Tugenden, Singular: Virtus) sind Attribute, die die angeborenen
		Fähigkeiten eines Charakters beschreiben. Sie werden bei der Charaktererschaffung benötigt, um
		die Werte der Peritiae zu bestimmen. Außerdem braucht ihr sie im Spiel, um eine Reihe von
		Aktionen auszuführen, die unter der Beschreibung jeder einzelnen Virtus aufgeführt sind. Es gibt
		drei Arten von Virtutes: des Körpers, des Geistes und der Seele.
	</p>
	<h2>Peritiae</h2>
	<p>
		Peritiae (deutsch: Kenntnisse, Singular: Peritia) sind Werte, die bestimmen, wie gut sich eure
		Charaktere durch Lernen, Erfahrung oder Ausbildung auf einem bestimmten Gebiet auskennen.
		Peritiae werden immer dann herangezogen, wenn ein*e Custos versucht, etwas durch eine Aktion zu
		erreichen, zum Beispiel, wenn er*sie ein Weissagungsritual ausführen, ein Publikum durch eine
		Theateraufführung beeindrucken oder eine Keule in das Flammenmaul einer Chimäre werfen möchte.
		Ein hoher Wert in einer Peritia entstammt meistens der Ausbildung, die die Custodes für ihre
		Officia erhalten haben.
	</p>

	<h2>Virtutes des Körpers</h2>
	<h3 class="text-3xl text-black">Coordinatio (Koordination)</h3>
	<p>
		Beschreibt, wie fähig der Charakter in Bezug auf Geschicklichkeit, Schnelligkeit, Beweglichkeit
		und Gleichgewicht ist.
	</p>
	<p>
		Auf Coordinatio würfelt ihr zum Beispiel, um einem fallenden Stein auszuweichen, jemand anderem
		etwas weg zuschnappen oder bei Wind auf einem schmalen Grat zu balancieren. Vigor (Lebenskraft):
		Die Fähigkeiten des Charakters bezüglich Stärke, Ausdauer und körperlicher Gesundheit.
	</p>
	<h3 class="text-3xl text-black">Vigor (Lebenskraft)</h3>
	<p>Die Fähigkeiten des Charakters bezüglich Stärke, Ausdauer und körperlicher Gesundheit.</p>
	<p>
		Auf Vigor würfelt ihr, wenn ihr etwa eine Tür eintreten oder einen Marmorsarkophag anheben
		wollt, oder um den Auswirkungen von Gift, Schmerzen, Erschöpfung oder Schlafmangel zu
		widerstehen.
	</p>

	<h2>Virtutes des Geistes</h2>
	<h3 class="text-3xl text-black">Auctoritas (Autorität)</h3>
	<p>Die Kraft der Persönlichkeit des Charakters, seine Anziehungskraft und Selbstsicherheit.</p>
	<p>
		Um einen guten Eindruck zu machen, eine Würdenträgerin zu umgarnen oder andere durch die schiere
		Macht der eigenen Persönlichkeit zu etwas zu zwingen, müsst ihr auf auf Auctoritas würfeln.
	</p>
	<h3 class="text-3xl text-black">Ingenium (Intellekt)</h3>
	<p>
		Hier zeigen sich Aspekte der Intelligenz des Charakters, zum Beispiel seine Gewitztheit, die
		Fähigkeit, Schlussfolgerungen zu ziehen, und das Gedächtnis.
	</p>
	<p>
		Auf Ingenium würfelt ihr zum Beispiel, um euch an eine Anleitung zu erinnern, die ihr in einer
		Schriftrolle gelesen habt, um ein Gesicht wiederzuerkennen, das ihr erst einmal in einer
		Menschenmenge gesehen habt, oder um in den Symbolen einer uralten Wandmalerei ein Muster zu
		erkennen.
	</p>

	<h2>Virtutes des Seele</h2>
	<h3 class="text-3xl text-black">Ratio (Vernunft)</h3>
	<p>
		Die Neigung des Charakters dazu, rational zu denken, Ruhe zu bewahren und sich durch
		Willenskraft durchzusetzen.
	</p>
	<p>
		Einen Wurf auf Ratio benötigt ihr beispielsweise, um euch einem angreifenden Löwen in den Weg zu
		stellen, den meisten feindlichen Zaubern zu widerstehen oder euch anderen übernatürlichen
		Bedrohungen zu stellen.
	</p>
	<h3 class="text-3xl text-black">Sensibilitas (Einfühlungsvermögen)</h3>
	<p>
		Hier zeigt sich, wie aufmerksam der Charakter ist, wie viel Empathie er hat und wie gut er Magie
		und das Unsichtbare spüren kann.
	</p>
	<p>
		Auf Sensibilitas würfelt ihr, um zum Beispiel zu merken, dass sich jemand anschleicht, um zu
		erkennen, wenn sich jemand ungewöhnlich verhält, oder um eine geisterhafte Präsenz zu spüren.
	</p>

	<!--Pert-->
	<h2>Peritiae</h2>
	<h3 class="text-3xl text-black">De Bello (Kampf)</h3>
	<p>
		De Bello misst die Kampfbereitschaft und die strategische Gerissenheit des Charakters.
		Typischerweise haben Krieger*innen einen hohen Wert in dieser Peritia.
	</p>
	<p>
		Auf De Bello würfelt ihr, wenn sich eure Charaktere in der Arena Gladiatorinnen stellen oder
		Barbaren mit Pfeilen und Speeren spicken, wenn eine*r von euch das Contubernium taktisch berät
		oder sogar eine ganze Einheit für den Kampf in Formation bringt.
	</p>

	<h3 class="text-3xl text-black">De Magia (Magie)</h3>
	<p>
		De Magia misst das magische und religiöse Wissen eines Charakters, insbesondere die Fähigkeit
		der Weissagung. Typischerweise haben Seher*innen hohe Werte in dieser Peritia.
	</p>
	<p>
		Auf De Magia würfelt ihr, wenn euer Charakter in einem römischen oder fremden Tempel die
		richtigen rituellen Handlungen durchführen, ein Ritual ausführen möchte, versucht einen Traum zu
		deuten oder im Vogelflug oder den Innereien eines Opfertiers ein Omen zu erkennen.
	</p>

	<h3 class="text-3xl text-black">De Corpore (Körperlichkeit)</h3>
	<p>
		De Corpore drückt die Fähigkeit eines Charakters aus, körperlich vorbereitet zu sein. Die
		meisten Custodes verlassen sich auf einen hohen Wert in dieser Peritia, um zu überleben.
	</p>
	<p>
		Auf De Corpore würfelt ihr, wenn eure Charaktere in einem Sturm auf dem Meer an Land schwimmen,
		die verschlossene Tür zu einer Villa öffnen, an einer Wache vorbeischleichen oder Meister im
		Harpastum (einem besonders brutalen Ballspiel) werden wollen.
	</p>

	<h3 class="text-3xl text-black">De Natura (Natur)</h3>
	<p>
		De Natura zeigt die Fähigkeiten an, die ein Charakter im Zuge seiner Reisen durch das
		Kaiserreich und längerer Aufenthalte im Freien erworben hat. Typischerweise haben
		Kundschafter*innen einen hohen Wert in dieser Peritia.
	</p>
	<p>
		Auf De Natura würfelt ihr, wenn eure Charaktere in der Wüste nach Wasser und Nahrung suchen, auf
		Kamelen oder Pferden reiten wollen oder versuchen, den besten Platz für ein Nachtlager zu finden
		oder der Spur eines verwundeten Tiers oder eines Fliehenden zu folgen.
	</p>

	<h3 class="text-3xl text-black">De Scientia (Wissenschaft)</h3>
	<p>
		De Scientia ist die Gesamtheit der wissenschaftlichen Kenntnisse eines Charakters sowie seine
		Fähigkeit, dieses Wissen anzuwenden. Darunter fallen Disziplinen wie Naturphilosophie, Medizin,
		Astronomie, Geschichte und aristotelische Logik. Typischerweise haben Gelehrte einen hohen Wert
		in dieser Peritia.
	</p>
	<p>
		Auf De Scientia würfelt ihr, wenn euer Charakter versucht, Verwundete zu heilen, eine
		Belagerungsmaschine zu bauen oder die Todesursache einer Leiche anhand von Spuren und
		Verletzungen zu bestimmen
	</p>

	<h3 class="text-3xl text-black">De Societate (Sozialfähigkeiten)</h3>
	<p>
		De Societate misst die Fähigkeiten eines Charakters in Bezug auf gesellschaftliches Verhalten.
		Dadurch können sich eure Charaktere im Palast eines fremden Prinzen ebenso zuhause fühlen wie in
		den finsteren Gassen von Subura. Typischerweise haben Diplomat*innen einen hohen Wert in dieser
		Peritia.
	</p>
	<p>
		Auf De Societate würfelt ihr, wenn euer Charakter eine Rede halten oder mit einem Händler
		feilschen möchte, versucht, einen ausländischen Würdenträger oder eine Diebesfürstin angemessen
		zu begrüßen, beim Würfelspiel zu betrügen oder eine glaubhafte Lüge zu erzählen.
	</p>
