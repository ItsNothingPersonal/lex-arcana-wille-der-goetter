						Bedeutung zu finden. Seher*innen sehen Omina als den Ausdruck der gewundenen Wege des
						Schicksals oder des flatterhaften Willens der Götter. Wenn die Custodes Zeug*innen eines
						Phänomens werden, das sie für eine natürliche Manifestation einer göttlichen Macht
						halten (z. B. ein plötzlicher Blitzeinschlag, ein Tier mit ungewöhnlicher Fellfarbe oder
						ein Blutmond), können sie versuchen, dieses Omen zu deuten, um neue Einsichten für die
						laufende Untersuchung zu gewinnen.
					</p>

