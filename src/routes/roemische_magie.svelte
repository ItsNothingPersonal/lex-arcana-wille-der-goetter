						Dieses Ritual erlaubt dem durchführenden Charakter, herauszufinden, was in der näheren
						Vergangenheit an dem Ort passiert ist, an dem er sich gerade aufhält. Die Vision zeigt
						aber nur dann etwas Relevantes, wenn das vergangene Geschehnis etwas damit zu tun hat,
