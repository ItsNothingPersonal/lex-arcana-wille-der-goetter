				<h2>Auf De Magia würfeln</h2>
				<p>
					Wenn euer Charakter in LEX ARCANA die Kunst der Weissagung bemühen will, müsst ihr dafür
					auf De Magia würfeln. Um ein Ritual abzuhalten, die Gunst der Götter zuerkennen, oder ein
					Zeichen zu deuten, würfelt ihr gegen einen festgelegten Schwierigkeitsgrad.
				</p>

				<h3>Weissagungsrituale</h3>
				<p>
					Durch Weissagungsrituale können die Custodes (und andere Figuren) Informationen über
					Personen, Gegenstände oder Orte erhalten. Diese können räumlich entfernt sein von dem
					Charakter, der das Ritual durchführt, oder mit der Zukunft oder der Vergangenheit
					zusammenhängen. Alle Rituale sind komplexe Zeremonien, deshalb benötigt man dafür einen
					ruhigen Ort, Konzentration und mindestens eine Stunde Zeit.
				</p>

				<p>
					Damit auch nur die Chance besteht, ein erfolgreiches Ritual abzuhalten, muss der
					durchführende Charakter ein klares Ziel im Sinn haben. Spielmechanisch ausgedrückt:
					Der*die Spieler*in des Charakters, der das Ritual durchführt, muss dem Demiurgus eine
					spezifische Frage stellen, die sich auf eine bestimmte Person, einen Ort, ein Geschehnis
					oder einen Gegenstand bezieht.
				</p>

				<p>
					Ist das Ritual erfolgreich, hat der durchführende Charakter eine kurze Vision, die sich
					auf einer spiegelnden Oberfläche, in einer Flamme, in Rauch oder ähnlichem abzeichnet.
					Eure Charaktere können nicht in die Vision eingreifen oder anders mit ihr interagieren.
					Welche Informationen ihr aus der Vision zieht, liegt ganz bei euch – das Ritual verschafft
					euch nicht automatisch Informationen über die Vision hinaus.
				</p>

