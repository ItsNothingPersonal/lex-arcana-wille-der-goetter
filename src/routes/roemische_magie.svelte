				<h3>Zeichen deuten</h3>
				<p>
					Seher*innen lernen nicht nur, wie man Rituale abhält, sie erfahren außerdem, wie man die
					Bedeutung von natürlichen Zeichen und Träumen erkennt. Anders als für die zeitraubenden
					Weissagungsrituale braucht es nur ein paar Sekunden Konzentration, um Zeichen zu deuten.
				</p>
				<p>
					Eine erfolgreiche Würfelprobe erlaubt es dem*der Custos, ein Zeichen als günstig (fas)
					oder ungünstig (nefas) zu erkennen und es mit einer Handlung, einem Ort, einer Person oder
					einem Gegenstand in Verbindung zu bringen.
				</p>
				<div class="border-2 border-stone-400 shadow-2xl p-3 my-4">
					<h4>Traumdeutung (SG 6)</h4>
					<p>
						Viele Menschen betrachten Träume als ein beliebtes Kommunikationsmittel zwischen der
						Welt der Sterblichen und der Götterwelt. Wenn der Demiurgus einen ungewöhnlichen Traum
						beschreibt, können die Custodes versuchen, ihn zu deuten, um etwas über kommende
						Geschehnisse in Erfahrung zu bringen.
					</p>

					<h4>Omendeutung (SG 6)</h4>
					<p>
						Eure Charaktere können versuchen, in außergewöhnlichen, aber natürlichen Vorkommnissen,
