					Die militärische und diplomatische Macht des Kaiserreichs, sein unermesslicher
					Wissensschatz, die außergewöhnlich große Loyalität von so verschiedenen und so weit
					entfernten Völkern und Herrschern, die es genießt – das alles entstammt der römischen
					Magie. Seit der Gründung der Stadt kreisen die magischen Kräfte der Caesaren um die Kunst
